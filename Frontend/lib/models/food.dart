class Food {
  final int id;
  final String name;
  final double calories;
  final double proteinG;
  final double carbsG;
  final double fatG;
  final double servingSizeG;

  Food({
    required this.id,
    required this.name,
    required this.calories,
    required this.proteinG,
    required this.carbsG,
    required this.fatG,
    required this.servingSizeG,
  });
}